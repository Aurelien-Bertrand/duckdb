CREATE TABLE customer (
    C_CUSTKEY INTEGER,
    C_NAME VARCHAR,
    C_ADDRESS VARCHAR,
    C_CITY VARCHAR,
    C_NATION VARCHAR,
    C_REGION VARCHAR,
    C_PHONE VARCHAR,
    C_MKTSEGMENT VARCHAR
);
COPY customer FROM 'benchmark/ssb/data/sf100/customer.csv' (HEADER);

CREATE TABLE lineorder (
    LO_ORDERKEY INTEGER,
    LO_LINENUMBER SMALLINT,
    LO_CUSTKEY INTEGER,
    LO_PARTKEY INTEGER,
    LO_SUPPKEY INTEGER,
    LO_ORDERDATE DATE,
    LO_ORDERPRIORITY VARCHAR,
    LO_SHIPPRIORITY SMALLINT,
    LO_QUANTITY SMALLINT,
    LO_EXTENDEDPRICE INTEGER,
    LO_ORDTOTALPRICE INTEGER,
    LO_DISCOUNT SMALLINT,
    LO_REVENUE INTEGER,
    LO_SUPPLYCOST INTEGER,
    LO_TAX SMALLINT,
    LO_COMMITDATE DATE,
    LO_SHIPMODE VARCHAR
);
COPY lineorder FROM 'benchmark/ssb/data/sf100/lineorder.csv'  (HEADER);

CREATE TABLE part (
    P_PARTKEY INTEGER,
    P_NAME VARCHAR,
    P_MFGR VARCHAR,
    P_CATEGORY VARCHAR,
    P_BRAND VARCHAR,
    P_COLOR VARCHAR,
    P_TYPE VARCHAR,
    P_SIZE SMALLINT,
    P_CONTAINER VARCHAR
);
COPY part FROM 'benchmark/ssb/data/sf100/part.csv' (HEADER);

CREATE TABLE supplier (
    S_SUPPKEY INTEGER,
    S_NAME VARCHAR,
    S_ADDRESS VARCHAR,
    S_CITY VARCHAR,
    S_NATION VARCHAR,
    S_REGION VARCHAR,
    S_PHONE VARCHAR
);
COPY supplier FROM 'benchmark/ssb/data/sf100/supplier.csv' (HEADER);

CREATE TABLE date (
    D_DATEKEY DATE,
    D_DATE VARCHAR(18),
    D_DAYOFWEEK VARCHAR,
    D_MONTH VARCHAR,
    D_YEAR SMALLINT,
    D_YEARMONTHNUM INTEGER,
    D_YEARMONTH VARCHAR(7),
    D_DAYNUMINWEEK SMALLINT,
    D_DAYNUMINMONTH SMALLINT,
    D_DAYNUMINYEAR SMALLINT,
    D_MONTHNUMINYEAR SMALLINT,
    D_WEEKNUMINYEAR SMALLINT,
    D_SELLINGSEASON VARCHAR,
    D_LASTDAYINWEEKFL BOOLEAN,
    D_LASTDAYINMONTHFL BOOLEAN,
    D_HOLIDAYFL BOOLEAN,
    D_WEEKDAYFL BOOLEAN
);
COPY date FROM 'benchmark/ssb/data/sf100/date.csv' (HEADER);
